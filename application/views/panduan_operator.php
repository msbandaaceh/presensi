                                    <li>Pilih kegiatan yang ingin dilihat</li>
                                    <li>Klik tombol <strong>"Detail"</strong> untuk melihat peserta</li>
                                    <li>Anda dapat mencetak laporan kegiatan</li>
                                </ol>
