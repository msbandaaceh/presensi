                                <ol>
                                    <li>Tombol <strong>"Presensi Istirahat"</strong> akan muncul pada waktu istirahat</li>
                                    <li>Klik tombol untuk mulai istirahat (10 menit pertama)</li>
                                    <li>Klik tombol lagi untuk selesai istirahat (10 menit terakhir)</li>
                                    <li>Waktu istirahat berbeda untuk hari Jumat</li>
                                </ol>

                                <h4>5. Presensi Kegiatan Lainnya</h4>
                                <ol>
                                    <li>Tombol <strong>"Presensi Lainnya"</strong> muncul jika ada kegiatan yang dijadwalkan</li>
                                    <li>Pilih kegiatan dari dropdown yang tersedia</li>
                                    <li>Klik <strong>"Simpan Presensi"</strong> untuk menyimpan</li>
                                </ol>
