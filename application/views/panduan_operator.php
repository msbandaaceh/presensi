                                <ol>
                                    <li>Klik tombol <strong>"Presensi Kehadiran"</strong> di menu sidebar</li>
                                    <li>Sistem akan menampilkan modal presensi dengan peta lokasi</li>
                                    <li>Pastikan lokasi Anda berada dalam area yang ditentukan</li>
